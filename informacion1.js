const imagen3persona13=document.getElementById("imagen3persona13");
const imagen4persona13=document.getElementById("imagen4persona13");
const imagen5persona13=document.getElementById("imagen5persona13");

const apiURL='https://localhost:5001/hipermedia/Fotos';


let getPhoto13= async()=>{                            
    const response=await fetch (apiURL);
    const data =await response.json();
    nombre13.innerText=data[13].names;
    lastname13.innerText=data[13].lastName;
    age13.innerText=data[13].age;
    tittle13.innerText=data[13].tittle;
    descript13.innerText=data[13].descript;
    email13.innerText=data[13].email;
    social13.innerText=data[13].social;
    imagen1persona13.src=data[13].imagen1;
    imagen2persona13.src=data[13].imagen2;
    imagen3persona13.src=data[13].imagen3;
    imagen4persona13.src=data[13].imagen4;
    imagen5persona13.src=data[13].imagen5;
    console.log(data);
   
  }; getPhoto13();




